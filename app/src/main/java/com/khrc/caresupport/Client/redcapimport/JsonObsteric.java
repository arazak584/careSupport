package com.khrc.caresupport.Client.redcapimport;

public class JsonObsteric {

    private String tel;
    private String parity;
    private String gravidity;
    private String spontaneous_abortions;
    private String induced_abortions;

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }

    public String getParity() {
        return parity;
    }

    public void setParity(String parity) {
        this.parity = parity;
    }

    public String getGravidity() {
        return gravidity;
    }

    public void setGravidity(String gravidity) {
        this.gravidity = gravidity;
    }

    public String getSpontaneous_abortions() {
        return spontaneous_abortions;
    }

    public void setSpontaneous_abortions(String spontaneous_abortions) {
        this.spontaneous_abortions = spontaneous_abortions;
    }

    public String getInduced_abortions() {
        return induced_abortions;
    }

    public void setInduced_abortions(String induced_abortions) {
        this.induced_abortions = induced_abortions;
    }
}
